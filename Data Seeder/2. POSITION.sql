  P_POSITION_ID          NUMBER GENERATED ALWAYS AS IDENTITY ( START WITH 21 MAXVALUE 999999 MINVALUE 1 CYCLE CACHE 20 ORDER KEEP) NOT NULL,
  P_POSITION_DESC        VARCHAR2(100 BYTE)     NOT NULL,
  P_POSITION_DEPARTMENT  VARCHAR2(150 BYTE)     NOT NULL
)
